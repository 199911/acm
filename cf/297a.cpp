int cnt( string str) {
	int ans = 0;
	for( int i =0 ; i < str.length(); i++ ) 
		ans += str[i] == '1';
	return ans;
}

int ok( string s, string t ) {
	if ( cnt(s) & 1 ) return cnt(s) + 1 >= cnt(t);
	else return cnt(s) >= cnt(t);
}

